package mypackage4;

public class EntrenadorItem 
{
  String id;
  String nombre;
  String pais1;
  String pais2;

  public EntrenadorItem()
  {
  }

  public String getId()
  {
    return id;
  }

  public void setId(String newId)
  {
    id = newId;
  }

  public String getNombre()
  {
    return nombre;
  }

  public void setNombre(String newNombre)
  {
    nombre = newNombre;
  }

  public String getPais1()
  {
    return pais1;
  }

  public void setPais1(String newPais1)
  {
    pais1 = newPais1;
  }

  public String getPais2()
  {
    return pais2;
  }

  public void setPais2(String newPais2)
  {
    pais2 = newPais2;
  }
}